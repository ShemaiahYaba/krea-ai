const sidebarMenuButtonVariants = cva(
  "peer/menu-button flex w-full items-center gap-3 overflow-hidden rounded-md p-2 text-left text-sm outline-none ring-primary transition-all duration-300 focus-visible:ring-2 active:bg-accent disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      variant: {
        default: "hover:bg-accent hover:text-accent-foreground",
      },
      isActive: {
        true: "bg-accent text-accent-foreground font-medium",
        false: "text-muted-foreground hover:text-foreground",
      },
    },
    defaultVariants: {
      variant: "default",
      isActive: false,
    },
  }
)

const SidebarMenuButton = React.forwardRef<
  HTMLAnchorElement,
  React.ComponentProps<"a"> & {
    asChild?: boolean
    tooltip?: string | React.ComponentProps<typeof TooltipContent>
  } & VariantProps<typeof sidebarMenuButtonVariants>
>(
  (
    {
      asChild = false,
      isActive = false,
      variant,
      tooltip,
      className,
      children,
      ...props
    },
    ref
  ) => {
    const Comp = asChild ? Slot : "a"
    const { isMobile, state } = useSidebar()

    const button = (
      <Comp
        ref={ref}
        className={cn(
            sidebarMenuButtonVariants({ variant, isActive }),
            state === 'collapsed' && 'justify-center',
            className
        )}
        {...props}
      >
        {children}
        <span className={cn('truncate', state === 'collapsed' && 'hidden')}>{props.title}</span>
      </Comp>
    )

    if (!tooltip) {
      return button
    }
    
    if (typeof tooltip === "string") {
        tooltip = {
            children: <div className="p-2">{tooltip}</div>,
        }
    }

    return (
      <Tooltip>
        <TooltipTrigger asChild>{button}</TooltipTrigger>
        <TooltipContent
          side="right"
          align="center"
          hidden={state !== "collapsed" || isMobile}
          className="border-none bg-accent text-accent-foreground rounded-md"
          {...tooltip}
        />
      </Tooltip>
    )
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

